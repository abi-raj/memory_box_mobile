import 'package:flutter/material.dart';

TextStyle textStyleWhite(weight, size) {
  return TextStyle(
    color: Colors.white,
    fontWeight: weight,
    fontSize: size,
  );
}

TextStyle textStyleBlack(weight, size) {
  return TextStyle(
    color: Colors.black,
    fontWeight: weight,
    fontSize: size,
  );
}
